enum custom_keycodes {
    CUST_RIGHT_ARROW = SAFE_RANGE,
    CUST_COLON_EQUAL
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) { 
    switch (keycode) {
        case CUST_RIGHT_ARROW:
            if (record->event.pressed) {
                SEND_STRING("=>");
            }
            return false;
            break;
        case CUST_COLON_EQUAL:
            if (record->event.pressed) {
                SEND_STRING(":=");
            }
            return false;
            break;
    }


    return true;
}

